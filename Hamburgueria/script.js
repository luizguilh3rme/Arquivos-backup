function promocoes(){
    var a = window.document.querySelector('.ai')
    var b = window.document.querySelector('#mostrar')
    var c = window.document.querySelector('#voltar')
    c.style.display = 'block'
    a.style.display = 'none'

    if(a.style.display == 'none'){
       b.style.display = 'block';
    }

    else{
        a.style.display == 'none'
    }
       
}

function voltar(){
    var V = window.document.querySelector('#mostrar')
    var v = window.document.querySelector('.ai')
    V.style.display = 'none'

    if(V.style.display == 'none'){

        v.style.display = 'block'
    }

    else{
        V.style.display == 'none'
    }
}





